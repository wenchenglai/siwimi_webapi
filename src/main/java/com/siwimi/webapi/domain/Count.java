package com.siwimi.webapi.domain;

public class Count {
	
	private String id;	
	private int count =0;
	

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
